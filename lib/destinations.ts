function img(slug: string): string {
  try {
    const p = path.join(process.cwd(), 'public', 'images-manifest.json')
    const raw = fs.readFileSync(p, 'utf-8')
    const m = JSON.parse(raw ?? '{}') as Record<string, string>
    return m?.[slug] ?? ''
  } catch {
    return ''
  }
}

